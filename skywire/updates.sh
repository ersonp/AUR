#!/bin/bash
#re-archive the scripts and update the checksums, etc.
tar -czvf skywire-deb-scripts.tar.gz skywire-deb-scripts
tar -czvf skywire-scripts.tar.gz skywire-scripts
updpkgsums cc.deb.PKGBUILD
updpkgsums deb.PKGBUILD
updpkgsums git.PKGBUILD
sed -i "s/pkgver=0.6.0/pkgver='autogenerated'/" git.PKGBUILD
updpkgsums
makepkg --printsrcinfo > .SRCINFO
echo "git add -f cc.deb.PKGBUILD cc.deb.PKGBUILD git.PKGBUILD PKGBUILD .SRCINFO skywire-scripts.tar.gz skywire-deb-scripts.tar.gz updates.sh"
